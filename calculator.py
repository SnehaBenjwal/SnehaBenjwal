first=input()
second=input()
operator=input()
first=int(first)
second=int(second)
if operator =="+":
    print(first+second)
elif operator =="-":
    print(first-second)
elif operator =="*":
    print(first*second)
elif operator =="/":
    print(first/second)
elif operator== "%":
    print(first%second)
else:
    print("Invalid operator")
     

    
    
    
    